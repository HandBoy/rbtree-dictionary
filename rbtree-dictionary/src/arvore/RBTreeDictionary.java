package arvore;

import utils.Node;

public class RBTreeDictionary {
	private Node raiz;
	private int tamanho;
	
	public Node getRaiz() {
		return raiz;
	}
	public void setRaiz(Node raiz) {
		this.raiz = raiz;
	}
	public int getTamanho() {
		return tamanho;
	}
	public void setTamanho(int tamanho) {
		this.tamanho = tamanho;
	}
	public void growUp(){
		this.tamanho++;
	}
	
	
}
